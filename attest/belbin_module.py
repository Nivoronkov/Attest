    def validate_block_points(self, points_list):
        """Проверяет, что в блоке ровно 8 значений и их сумма строго равна 10."""
        return len(points_list) == 8 and sum(points_list) == 10 and all(p >= 0 for p in points_list)

    def calculate_results(self, all_answers):
        """
        all_answers: словарь {номер_блока: [8 чисел — баллы за утверждения А..З]}
        Возвращает словарь {название_роли: суммарный_балл}, сумма всех баллов = 70.
        """
        raw_results = {code: 0 for code in self.roles_keys.values()}

        for block_num, points in all_answers.items():
            if block_num not in self.scoring_key:
                continue
            block_key = self.scoring_key[block_num]
            for i, point in enumerate(points):
                raw_results[block_key[i]] += point

        return {role_name: raw_results[code] for role_name, code in self.roles_keys.items()}

    def get_dominant_roles(self, results, top_n=2):
        """Возвращает список из top_n ведущих ролей (по убыванию баллов)."""
        sorted_roles = sorted(results.items(), key=lambda item: item[1], reverse=True)
        return [role for role, _ in sorted_roles[:top_n]]


