    def _add_key(self, q_num, factor, key_letter):
        """Добавляет ключ вида: крайний ответ в ключевом направлении - 2 балла, 'b' - 1 балл."""
        entry = {'factor': factor, 'a': 0, 'b': 1, 'c': 0}
        entry[key_letter] = 2
        self.scoring_keys[q_num] = entry

    @staticmethod
    def _make_sten_table(ranges):
        """Превращает список диапазонов (от, до, стен) в словарь {сырой_балл: стен}."""
        table = {}
        for low, high, sten in ranges:
            for raw in range(low, high + 1):
                table[raw] = sten
        return table

    # ----------------------------------------------------------------------
    # Основные методы подсчёта
    # ----------------------------------------------------------------------
    def calculate_raw_scores(self, user_answers):
        """
        user_answers: словарь {номер_вопроса: 'a'|'b'|'c'}
        Возвращает словарь сырых баллов по 16 факторам и шкале MD.
        """
        raw_scores = {factor: 0 for factor in self.factors}
        raw_scores['MD'] = 0

        for q_num, answer in user_answers.items():
            if q_num not in self.scoring_keys or answer not in ('a', 'b', 'c'):
                continue
            key_data = self.scoring_keys[q_num]
            raw_scores[key_data['factor']] += key_data[answer]

        return raw_scores

    def convert_to_stens(self, raw_scores):
        """Переводит сырые баллы 16 факторов в стены (шкала 1-10). MD остаётся в сырых баллах."""
        stens = {}
        for factor in self.factors:
            raw = raw_scores.get(factor, 0)
            table = self.sten_tables[factor]
            if raw in table:
                stens[factor] = table[raw]
            elif raw < min(table):
                stens[factor] = 1
            else:
                stens[factor] = 10
        return stens

    def check_validity(self, raw_scores):
        """
        Проверка достоверности по шкале MD (максимум 14 сырых баллов).
        MD >= 9 — результаты сомнительны: выражено стремление выглядеть лучше.
        """
        md = raw_scores.get('MD', 0)
        if md >= 9:
            return (False, f"Шкала MD = {md} из 14: выражено стремление показать себя "
                           f"в благоприятном свете. Результаты следует считать недостоверными "
                           f"и интерпретировать с большой осторожностью.")
        elif md >= 6:
            return (True, f"Шкала MD = {md} из 14: умеренная тенденция к социально "
                          f"желательным ответам, результаты в целом достоверны.")
        return (True, f"Шкала MD = {md} из 14: самооценка адекватна, результаты достоверны.")

    def interpret_factor(self, factor, sten):
        """Возвращает текстовую интерпретацию фактора по величине стена."""
        low, mid, high = self.interpretations[factor]
        if sten <= 3:
            return low
        elif sten <= 7:
            return mid
        return high

    def get_full_results(self, user_answers):
        """
        Полный цикл обработки: сырые баллы -> стены -> проверка достоверности.
        Возвращает словарь с ключами 'raw', 'stens', 'valid', 'validity_note'.
        """
        raw = self.calculate_raw_scores(user_answers)
        stens = self.convert_to_stens(raw)
        valid, note = self.check_validity(raw)
        return {'raw': raw, 'stens': stens, 'valid': valid, 'validity_note': note}


