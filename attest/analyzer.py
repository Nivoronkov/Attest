    HIGH = 7   # стен >= 7 считается высоким
    LOW = 4    # стен <= 4 считается низким

    def __init__(self, name, dept, belbin_results, cattell_results,
                 belbin_test=None, cattell_test=None):
        """
        name, dept        - ФИО и должность/отдел сотрудника;
        belbin_results    - словарь {роль: баллы} (сумма 70);
        cattell_results   - словарь из CattellTest.get_full_results():
                            {'raw':..., 'stens':..., 'valid':..., 'validity_note':...};
        belbin_test       - экземпляр BelbinTest (для описаний ролей), опционально;
        cattell_test      - экземпляр CattellTest (для интерпретаций), опционально.
        """
        self.name = name
        self.dept = dept
        self.belbin_roles = belbin_results
        self.cattell = cattell_results
        self.stens = cattell_results['stens']
        self.belbin_test = belbin_test
        self.cattell_test = cattell_test

    # ------------------------------------------------------------------
    def get_dominant_roles(self, top_n=2):
        sorted_roles = sorted(self.belbin_roles.items(), key=lambda item: item[1], reverse=True)
        return [role for role, _ in sorted_roles[:top_n]]

    def _check_condition(self, factor, direction):
        sten = self.stens.get(factor)
        if sten is None:
            return False
        return sten >= self.HIGH if direction == 'high' else sten <= self.LOW

    # ------------------------------------------------------------------
    def analyze_role(self, role):
        """Возвращает (статус, текст) сопоставления одной роли с профилем Кеттелла."""
        rules = self.ROLE_RULES.get(role)
        if rules is None:
            return ('нет данных', f"Для роли «{role}» правила сопоставления не заданы.")

        confirmed = sum(1 for f, d in rules['confirm'] if self._check_condition(f, d))
        contradicted = any(self._check_condition(f, d) for f, d in rules['contradict'])

        if contradicted:
            return ('ПРОТИВОРЕЧИЕ', rules['contradict_text'])
        if confirmed >= rules['confirm_min']:
            return ('ПОДТВЕРЖДЕНО', rules['confirm_text'])
        if confirmed > 0:
            return ('ЧАСТИЧНО ПОДТВЕРЖДЕНО',
                    "Часть личностных факторов поддерживает эту роль, однако выраженного "
                    "подтверждения по Кеттеллу нет. Роль, вероятно, опирается на опыт и "
                    "выработанные навыки; в стрессовых условиях устойчивость роли снижена.")
        return ('НЕЙТРАЛЬНО',
                "Личностный профиль не даёт ни подтверждения, ни опровержения этой роли: "
                "соответствующие факторы находятся в средней зоне. Роль работоспособна, "
                "но не является природной сильной стороной.")

    # ------------------------------------------------------------------
    def general_conclusions(self):
        """Общие выводы по профилю Кеттелла безотносительно ролей Белбина."""
        s = self.stens
        notes = []

        # Эмоциональный блок
        anxiety = [s.get('C', 5) <= self.LOW, s.get('O', 5) >= self.HIGH, s.get('Q4', 5) >= self.HIGH]
        if sum(anxiety) >= 2:
            notes.append("Эмоциональный блок (C-, O+, Q4+): повышенный уровень тревожности и "
                         "напряжённости. Сотруднику противопоказана длительная работа в режиме "
                         "аврала; рекомендуется чёткая постановка задач и предсказуемая среда.")
        elif s.get('C', 5) >= self.HIGH and s.get('O', 5) <= self.LOW:
            notes.append("Эмоциональный блок: высокая стрессоустойчивость и уверенность в себе. "
                         "Сотрудника можно задействовать в напряжённых проектах и работе с претензиями.")

        # Коммуникативный блок
        comm = [s.get('A', 5) >= self.HIGH, s.get('H', 5) >= self.HIGH, s.get('F', 5) >= self.HIGH]
        if sum(comm) >= 2:
            notes.append("Коммуникативный блок (A+, H+, F+): выраженный коммуникативный потенциал. "
                         "Целесообразно использовать в клиентской работе, переговорах, наставничестве.")
        elif s.get('A', 5) <= self.LOW and s.get('H', 5) <= self.LOW:
            notes.append("Коммуникативный блок (A-, H-): интенсивное общение истощает сотрудника. "
                         "Эффективнее всего он будет в самостоятельной экспертной работе.")

        # Интеллектуальный блок
        if s.get('B', 5) >= self.HIGH and (s.get('M', 5) >= self.HIGH or s.get('Q1', 5) >= self.HIGH):
            notes.append("Интеллектуальный блок (B+, M+/Q1+): высокий потенциал к аналитической и "
                         "творческой работе, быстрая обучаемость. Рекомендуется включать в кадровый "
                         "резерв на интеллектуально ёмкие направления.")

        # Регуляторный блок
        if s.get('Q3', 5) >= self.HIGH and s.get('G', 5) >= self.HIGH:
            notes.append("Регуляторный блок (Q3+, G+): высокая организованность и ответственность. "
                         "Сотруднику можно делегировать задачи, требующие самостоятельного контроля качества.")
        elif s.get('Q3', 5) <= self.LOW and s.get('G', 5) <= self.LOW:
            notes.append("Регуляторный блок (Q3-, G-): слабая саморегуляция. Необходим внешний "
                         "контроль сроков и качества, чёткие регламенты.")

        # Лидерский потенциал
        if s.get('E', 5) >= self.HIGH and s.get('H', 5) >= self.HIGH and s.get('C', 5) >= self.HIGH:
            notes.append("Лидерский потенциал (E+, H+, C+): есть природные предпосылки к руководящей "
                         "работе; целесообразно рассмотреть в резерв на управленческие позиции.")

        if not notes:
            notes.append("Профиль сбалансирован, выраженных зон риска и ярких пиков не выявлено: "
                         "сотрудник универсален, устойчив в большинстве рабочих контекстов.")
        return notes

    # ------------------------------------------------------------------
    def analyze_intersections(self):
        """Формирует полный текстовый отчёт объединённого анализа."""
        report = []
        report.append("=" * 62)
        report.append("ОБЪЕДИНЁННЫЙ ОТЧЁТ ПО РЕЗУЛЬТАТАМ АТТЕСТАЦИИ")
        report.append("=" * 62)
        report.append(f"Сотрудник: {self.name}")
        report.append(f"Должность/отдел: {self.dept}")
        report.append("")

        # --- Достоверность ---
        report.append("ДОСТОВЕРНОСТЬ РЕЗУЛЬТАТОВ")
        report.append(self.cattell['validity_note'])
        report.append("")

        # --- Профиль Кеттелла ---
        report.append("-" * 62)
        report.append("I. ЛИЧНОСТНЫЙ ПРОФИЛЬ ПО КЕТТЕЛЛУ (стены 1-10)")
        report.append("-" * 62)
        for factor, sten in self.stens.items():
            name = (self.cattell_test.factor_names[factor]
                    if self.cattell_test else factor)
            bar = "█" * sten + "·" * (10 - sten)
            report.append(f"{name}")
            report.append(f"   {bar}  {sten} стен")
            if self.cattell_test:
                report.append(f"   {self.cattell_test.interpret_factor(factor, sten)}")
            report.append("")

        # --- Профиль Белбина ---
        report.append("-" * 62)
        report.append("II. КОМАНДНЫЕ РОЛИ ПО БЕЛБИНУ (сумма баллов = 70)")
        report.append("-" * 62)
        dominant = self.get_dominant_roles()
        for role, score in sorted(self.belbin_roles.items(), key=lambda x: -x[1]):
            marker = " ← ведущая роль" if role in dominant else ""
            report.append(f"{role:<16} {score:>3} баллов{marker}")
        report.append("")
        for role in dominant:
            if self.belbin_test:
                report.append(f"«{role}»: {self.belbin_test.role_descriptions[role]}")
                report.append("")

        # --- Пересечение методик ---
        report.append("-" * 62)
        report.append("III. СОПОСТАВЛЕНИЕ: ПОДТВЕРЖДАЮТСЯ ЛИ РОЛИ ЛИЧНОСТНЫМ ПРОФИЛЕМ")
        report.append("-" * 62)
        for role in dominant:
            status, text = self.analyze_role(role)
            report.append(f"[РОЛЬ «{role.upper()}» — {status}]")
            report.append(text)
            report.append("")

        # --- Общие выводы ---
        report.append("-" * 62)
        report.append("IV. ОБЩИЕ ВЫВОДЫ И РЕКОМЕНДАЦИИ")
        report.append("-" * 62)
        for i, note in enumerate(self.general_conclusions(), 1):
            report.append(f"{i}. {note}")
        report.append("")
        report.append("Отчёт сформирован автоматически по результатам двух методик и носит "
                      "рекомендательный характер. Кадровые решения должны приниматься с учётом "
                      "интервью и рабочих показателей сотрудника.")

        return "\n".join(report)


