    def validate_block_points(self, points_list):
        """Проверяет, что сумма баллов в блоке строго равна 10"""
        return sum(points_list) == 10

    def calculate_results(self, all_answers):
        """
        all_answers: словарь вида {номер_блока: [список из 8 чисел (баллов)]}
        Возвращает: словарь с суммами баллов по каждой текстовой роли
        """
        # Инициализируем итоговые баллы нулями для аббревиатур
        raw_results = {code: 0 for code in self.roles_keys.values()}
        
        # Считаем баллы
        for block_num, points in all_answers.items():
            block_key = self.scoring_key[block_num]
            for i, point in enumerate(points):
                role_code = block_key[i]
                raw_results[role_code] += point
                
        # Переводим аббревиатуры в понятные названия ролей
        final_results = {}
        for role_name, code in self.roles_keys.items():
            final_results[role_name] = raw_results[code]
            
        return final_results

